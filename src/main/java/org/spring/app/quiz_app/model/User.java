package org.spring.app.quiz_app.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "users")
public class User {
    @Id
    private String id;
    private String email;
    private String password; // For demo purposes; in production hash this
    private String role = "USER"; // USER or ADMIN
    private boolean takenQuiz = false;

    public User() {}

    public User(String email, String password, String role) {
        this.email = email;
        this.password = password;
        this.role = role;
    }

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPassword() { return password; }
    public void setPassword(String password) { this.password = password; }

    public String getRole() { return role; }
    public void setRole(String role) { this.role = role; }

    public boolean isTakenQuiz() { return takenQuiz; }
    public void setTakenQuiz(boolean takenQuiz) { this.takenQuiz = takenQuiz; }
}
